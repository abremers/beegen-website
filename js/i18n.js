/* ── Translation Engine ───────────────────────────────────── */
function bgGetKey(obj, path) {
  return path.split('.').reduce((o, k) => (o && o[k] !== undefined ? o[k] : null), obj);
}

function bgSetLang(lang) {
  const t = BGI18N[lang];
  if (!t) return;

  // Save preference
  try { localStorage.setItem('bLang', lang); } catch(e) {}

  // Set html lang attribute
  document.documentElement.lang = lang === 'es' ? 'es' : 'en';

  // Text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = bgGetKey(t, el.dataset.i18n);
    if (val !== null) el.textContent = val;
  });

  // Inner HTML (for tags like <br>)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const val = bgGetKey(t, el.dataset.i18nHtml);
    if (val !== null) el.innerHTML = val;
  });

  // Placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const val = bgGetKey(t, el.dataset.i18nPlaceholder);
    if (val !== null) el.placeholder = val;
  });

  // Aria-label attributes
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const val = bgGetKey(t, el.dataset.i18nAria);
    if (val !== null) el.setAttribute('aria-label', val);
  });

  // Lang toggle label: show opposite language
  document.querySelectorAll('.lang-label').forEach(el => {
    el.textContent = lang === 'es' ? 'EN' : 'ES';
  });
}

/* ── Init ─────────────────────────────────────────────────── */
(function () {
  let saved;
  try { saved = localStorage.getItem('bLang'); } catch(e) {}
  const lang = (saved === 'en' || saved === 'es') ? saved : 'es'; // default: Spanish
  bgSetLang(lang);

  document.querySelectorAll('.lang-toggle').forEach(btn => {
    btn.addEventListener('click', () => {
      let cur;
      try { cur = localStorage.getItem('bLang'); } catch(e) {}
      bgSetLang((cur === 'es') ? 'en' : 'es');
    });
  });
})();
